// What we are working with here is called JSX. It looks very similar to HTML,
// but is in fact JavaScript. We use it to build the structure of our application,
// just as we would use HTML.

// We can use any standard HTML elements within JSX: divs, any heading element, paragraph, spans, buttons, and so on.
// It's important to note that there are some minor differences between JSX and HTML.

// The attributes that we use on JSX are slightly different than in normal HTML elements.
// They are written in the camelcase style, which is a standard way of writing variables or properties in JavaScript.

// For example, to apply a class on a JSX element, we use an attribute called className.
// For normal HTML, that would just be called class.
